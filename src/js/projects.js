function disableHover(id) {
        document.getElementById(id).classList.add('disable-hover');
}

function onClose(id) {
        document.getElementById(id).classList.add('on-close');
}

function onCloseLeft(id) {
        document.getElementById(id).classList.add('on-close-left');
}

function onCloseRight(id) {
        document.getElementById(id).classList.add('on-close-right');
}
